static int address_matches(struct address *addr, const char *addrstr_expect)
{
    char addrstr[64];
    const char *dst;

    if (addr->family != AF_INET6) {
        fprintf(stderr, "Failed: unexpected family %d\n", addr->family);
        return 1;
    }

    dst = inet_ntop(AF_INET6, (void *)&addr->ip6_addr, addrstr, sizeof(addrstr));
    if (!dst) {
        fprintf(stderr, "Failed: missing address\n");
        return 1;
    }

    if (strcmp(addrstr, addrstr_expect)) {
        fprintf(stderr, "Failed: invalid address: \"%s\"\n", addrstr);
        return 1;
    }

    return 0;
}

int main(void)
{
    int r;
    struct dns_info info;

    r = parse_ip_dns(ip_packet, sizeof(ip_packet), &info);
    if (r != 1) {
        fprintf(stderr, "Failed: return code is %d\n", r);
        return 1;
    }

    if (strcmp(info.name, "irc.geo.oftc.net")) {
        fprintf(stderr, "Failed: name is \"%s\"\n", info.name);
        return 1;
    }

    if (info.count != 4) {
        fprintf(stderr, "Failed: invalid addresses count %d\n", info.count);
        return 1;
    }

    address_matches(&info.entries[0], "2a01:7e00::f03c:91ff:fe96:d48a");
    address_matches(&info.entries[1], "2001:6b0:e:2a18::116");
    address_matches(&info.entries[2], "2001:1b40:5000:fe0::6667");
    address_matches(&info.entries[3], "2a01:238:42b4:1600:abcd:efab:6667:6697");

    puts("Passed");
    return 0;
}
